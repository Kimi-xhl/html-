// + - * /
console.log('Example');
var x  = 10
console.log('x+1=',x+1);
console.log('x-1=',x-1);
console.log('x/5=',x/5);
console.log('x*5=',x*5);
// % 取模
console.log('x%5=',x%5);
// ++ 自增 (0和正整数)
console.log('_______________________________');
var y = 6
console.log(y++) // 6 运行结束之后y+1 先执行 后自增
console.log(y);
var y1 = 6
console.log(++y1); // 先自增 再执行
console.log(y1);
// += -= *= /= %=
var k = 6;
k = k + 1 // 7
k = k * 2 // 14
console.log(k);
// console.log(k = k*j);
var j = 6
j += 1 // 相当于 j = j + 1
j *= 1 // 相当于 j = j * 2
console.log(j);
